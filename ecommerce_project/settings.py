# Database - Neon PostgreSQL
DATABASE_URL = os.getenv('DATABASE_URL', '')

if DATABASE_URL:
    import re
    match = re.match(
        r'postgresql://(?P<user>[^:]+):(?P<password>[^@]+)@(?P<host>[^/]+)/(?P<name>[^?]+)(?:\?(?P<params>.+))?',
        DATABASE_URL
    )
    if match:
        db_parts = match.groupdict()
        DATABASES = {
            'default': {
                'ENGINE': 'django.db.backends.postgresql',
                'NAME': db_parts['name'],
                'USER': db_parts['user'],
                'PASSWORD': db_parts['password'],
                'HOST': db_parts['host'].split(':')[0],
                'PORT': db_parts['host'].split(':')[1] if ':' in db_parts['host'] else '5432',
                'OPTIONS': {
                    'sslmode': 'require',
                },
            }
        }
    else:
        DATABASES = {
            'default': {
                'ENGINE': 'django.db.backends.sqlite3',
                'NAME': BASE_DIR / 'db.sqlite3',
            }
        }
else:
    DATABASES = {
        'default': {
            'ENGINE': 'django.db.backends.sqlite3',
            'NAME': BASE_DIR / 'db.sqlite3',
        }
    }

